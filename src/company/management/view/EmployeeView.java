    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:   
        id = Integer.parseInt(txtID.getText());
        name = txtName.getText();
        email = txtEmail.getText();
        branch = cmbBranch.getSelectedItem().toString();
        designation = cmbDesignation.getSelectedItem().toString();
        salary = txtSalary.getText();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/swing_demo", "root", "1234");
            String sq = "INSERT INTO employee" + "(id,name,email,branch,designation,salary,gender)" + "VALUES (?,?,?,?,?,?,?)";
            PreparedStatement st = con.prepareStatement(sq);
            st.setInt(1, id);
            st.setString(2, name);
            st.setString(3, email);
            st.setString(4, branch);
            st.setString(5, designation);
            st.setString(6, salary);
            st.setString(7, gender);
            st.executeUpdate();
            st.close();
            JOptionPane.showMessageDialog(null, "Data Inserted Successfully!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data does not inserted" + e);
        }


    }//GEN-LAST:event_btnAddActionPerformed

