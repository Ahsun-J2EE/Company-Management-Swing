    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
            String a = txtID.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/swing_demo", "root", "1234");
            String sql = "select * from employee where  id='" + a + "'";
            Statement st = con.createStatement();
            //pst.setString(1, u);

            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                txtName.setText(rs.getString("name"));
                txtEmail.setText(rs.getString("email"));
                cmbBranch.setSelectedItem(rs.getString("branch"));
                cmbDesignation.setSelectedItem(rs.getString("designation"));
                txtSalary.setText(rs.getString("salary"));

            } else {
                JOptionPane.showMessageDialog(rootPane, "No Data Found!");

            }

        } catch (Exception ex) {
            //Logger.getLogger(RemoveEmployee.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("The error is:" + ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

